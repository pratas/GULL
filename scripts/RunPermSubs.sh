#!/bin/bash
###############################################################################
# PARAMETERS ==================================================================
GET_GULL=0;
GET_XS=1;
GET_GOOSE=0;
RUN_XS=1;
SHUFFLE=1;
RUN_GULL=1;
###############################################################################
# GET GOOSE ===================================================================
if [[ "$GET_GOOSE" -eq "1" ]]; then
  rm -fr goose-*;
  git clone https://github.com/pratas/goose.git
  cd goose/src/
  make
  cd ../../
  cp goose/src/goose-* .
fi
###############################################################################
# GET GULL ====================================================================
if [[ "$GET_GULL" -eq "1" ]]; then
  rm -fr GULL-*;
  git clone https://github.com/pratas/GULL.git
  cd GULL/src/
  cmake .
  make
  cd ../../
  cp GULL/src/GULL-* .
fi
###############################################################################
# GET XS ======================================================================
if [[ "$GET_XS" -eq "1" ]]; then
  rm -fr XS*;
  git clone https://github.com/pratas/xs.git
  cd xs/
  make
  cd ../
  cp xs/XS .
fi
###############################################################################



